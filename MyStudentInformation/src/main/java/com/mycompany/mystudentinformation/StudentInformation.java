    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        // TODO add your handling code here:
   
    // Get the selected index from the list
    int selectedIndex = studentList.getSelectedIndex();
    
    // Check if a valid item is selected
    if (selectedIndex != -1) {
        // Remove the selected item from the list model
        studentListModel.remove(selectedIndex);
    } else {
        // Show an error message if nothing is selected
        JOptionPane.showMessageDialog(this, "Please select a student to remove.", "Selection Error", JOptionPane.ERROR_MESSAGE);
    }

      
    }//GEN-LAST:event_btnRemoveActionPerformed

