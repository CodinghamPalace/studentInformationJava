    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
    try {
        String studentID = txtfieldStudentID.getText();
        String name = txtfieldStudentName.getText();
        int age = Integer.parseInt(txtfieldAge.getText());
        String address = txtfieldAddress.getText();
        double gpa = Double.parseDouble(txtfieldGPA.getText());
        String course = txtfieldCourse.getText();

        // Validation
        if (age < 0) throw new IllegalArgumentException("Age cannot be negative");
        if (gpa < 0.0 || gpa > 4.0) throw new IllegalArgumentException("GPA must be between 0.0 and 4.0");

        // Create a Student object
        Student student = new Student(name, age, address, studentID, gpa, course);

        // Update labels
        labelStudentID.setText(student.getStudentID());
        labelStudentName.setText(student.getName());
        labelAge.setText(String.valueOf(student.getAge()));
        labelAddress.setText(student.getAddress());
        labelGPA.setText(String.valueOf(student.getGPA()));
        labelCourse.setText(student.getCourse());

        // Add student to the list
        DefaultListModel<String> model = (DefaultListModel<String>) studentList.getModel();
        model.addElement(student.getName() + " - " + student.getCourse());
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Please enter valid numbers for age and GPA", "Input Error", JOptionPane.ERROR_MESSAGE);
    } catch (IllegalArgumentException e) {
        JOptionPane.showMessageDialog(this, e.getMessage(), "Input Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_btnSaveActionPerformed

